tflite::MicroErrorReporter tflErrorReporter;
tflite::AllOpsResolver tflOpsResolver;

const tflite::Model *tflModel = nullptr;
tflite::MicroInterpreter *tflInterpreter = nullptr;
TfLiteTensor *tflInputTensor = nullptr;
TfLiteTensor *tflOutputTensor = nullptr;

constexpr int tensorArenaSize = 64 * 1024;
byte tensorArena[tensorArenaSize] __attribute__((aligned(16)));

const char *GESTURES[NUM_GESTURES] = {
    "Eating",
    "Idle",
    "Walking"};

uint8_t ai_state = 1; 
static int sampleIndex = 0;

void ai_init()
{
    tflModel = tflite::GetModel(model);
    if (tflModel->version() != TFLITE_SCHEMA_VERSION)
    {
        Serial.println("Model schema mismatch!");
        while (1);
    }

    tflInterpreter = new tflite::MicroInterpreter(tflModel, tflOpsResolver, tensorArena, tensorArenaSize, &tflErrorReporter);
    tflInterpreter->AllocateTensors();

    tflInputTensor = tflInterpreter->input(0);
    tflOutputTensor = tflInterpreter->output(0);
}

uint8_t ai_detect(float aX, float aY, float aZ)
{
    // Nếu buffer chưa đầy, cứ thêm dữ liệu vào
    if (sampleIndex < NUM_SAMPLES) {
        tflInputTensor->data.f[sampleIndex * 3 + 0] = aX;
        tflInputTensor->data.f[sampleIndex * 3 + 1] = aY;
        tflInputTensor->data.f[sampleIndex * 3 + 2] = aZ;
        sampleIndex++;
        return ai_state;  // Chưa đủ dữ liệu, chưa dự đoán
    }

    // Buffer đầy, thực hiện dịch dữ liệu (sliding window)
    for (int i = 1; i < NUM_SAMPLES; i++) {
        tflInputTensor->data.f[(i - 1) * 3 + 0] = tflInputTensor->data.f[i * 3 + 0];
        tflInputTensor->data.f[(i - 1) * 3 + 1] = tflInputTensor->data.f[i * 3 + 1];
        tflInputTensor->data.f[(i - 1) * 3 + 2] = tflInputTensor->data.f[i * 3 + 2];
    }

    // Thêm mẫu mới vào cuối buffer
    tflInputTensor->data.f[(NUM_SAMPLES - 1) * 3 + 0] = aX;
    tflInputTensor->data.f[(NUM_SAMPLES - 1) * 3 + 1] = aY;
    tflInputTensor->data.f[(NUM_SAMPLES - 1) * 3 + 2] = aZ;

    // Chạy mô hình sau mỗi bước dịch
    if (tflInterpreter->Invoke() != kTfLiteOk)
    {
        Serial.println("Invoke failed!");
        return ai_state;
    }

    // Tìm nhãn có xác suất cao nhất
    int maxIndex = 0;
    float maxValue = tflOutputTensor->data.f[0];

    for (int i = 1; i < NUM_GESTURES; i++)
    {
        if (tflOutputTensor->data.f[i] > maxValue)
        {
            maxValue = tflOutputTensor->data.f[i];
            maxIndex = i;
        }
    }

    Serial.print("Detected: ");
    Serial.println(GESTURES[maxIndex]);

    ai_state = maxIndex;

    return ai_state;
}

