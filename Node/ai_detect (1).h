    char* SerialReadLine(int timeout) {
        static char buffer[100];
        int index = 0;
        unsigned long startTime = millis();
        
        while (millis() - startTime < timeout) {
            if (Serial.available()) {
                char c = Serial.read();
                if (c == '\n' || c == '\r') {
                    buffer[index] = '\0';
                    return buffer;
                } else if (index < sizeof(buffer) - 1) {
                    buffer[index++] = c;
                }
            }
        }
        return nullptr;
    }
